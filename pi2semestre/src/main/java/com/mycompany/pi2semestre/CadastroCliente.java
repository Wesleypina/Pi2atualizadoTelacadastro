    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if(usuario == null) 
            Usuario.listaUsuarios.add(new Usuario(tfNome.getText(), tfEstadoCivil.getText(), tfSexo.getText(), 
                tfCPF.getText(), tfDataNascimento.getText(), tfTelefone.getText(), tfEmail.getText(), 
               tfLogradouro.getText(), Integer.parseInt(tfCEP.getText().replace("-", "")), tfBairro.getText(), 
               tfCidade.getText()));
        else            
            Usuario.listaUsuarios.add(Usuario.listaUsuarios.indexOf(usuario), new Usuario(tfNome.getText(), tfEstadoCivil.getText(), tfSexo.getText(), 
                tfCPF.getText(), tfDataNascimento.getText(), tfTelefone.getText(), tfEmail.getText(), null, usuario.getDinheiroGasto(),
                    usuario.getComprasRealizadas(), tfLogradouro.getText(), Integer.parseInt(tfCEP.getText().replace("-", "")), tfBairro.getText(), 
               tfCidade.getText(), usuario.getDataRegistro()));
        
           
        
       
        JPanelListagemProduto.jPanelListagemProduto.revalidate();
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

